package com.pk.completejava.controlflow;

public class Conditional {
    public static void main(String[] args) {
        // Conditional operators
        // ?: - Conditional operator
        int a = 10;
        int b = 20;
        int c = (a > b) ? a : b;
        System.out.println("The greater number is: " + c);
    }
}
