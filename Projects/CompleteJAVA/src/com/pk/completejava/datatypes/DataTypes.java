package com.pk.completejava.datatypes;
public class DataTypes {
    public static void main(String[] args) {
        System.out.println("Byte data type");
        System.out.println("Size: " + Byte.SIZE + " bits");
        System.out.println("Minimum byte value: " + Byte.MIN_VALUE);
        System.out.println("Maximum byte value: " + Byte.MAX_VALUE);
        System.out.println("Short data type");
        System.out.println("Size: " + Short.SIZE + " bits");
        System.out.println("Minimum short value: " + Short.MIN_VALUE);
        System.out.println("Maximum short value: " + Short.MAX_VALUE);
        System.out.println("Integer data type");
        System.out.println("Size: " + Integer.SIZE + " bits");
        System.out.println("Maximum integer value: " + Integer.MAX_VALUE);
        System.out.println("Minimum integer value: " + Integer.MIN_VALUE);
        System.out.println("Long data type");
        System.out.println("Size: " + Long.SIZE + " bits");
        System.out.println("Maximum long value: " + Long.MAX_VALUE);
        System.out.println("Minimum long value: " + Long.MIN_VALUE);
        System.out.println("Float data type");
        System.out.println("Size: " + Float.SIZE + " bits");
        System.out.println("Maximum float value: " + Float.MAX_VALUE);
        System.out.println("Minimum float value: " + Float.MIN_VALUE);
        System.out.println("Double data type");
        System.out.println("Size: " + Double.SIZE + " bits");
        System.out.println("Maximum double value: " + Double.MAX_VALUE);
        System.out.println("Minimum double value: " + Double.MIN_VALUE);
        System.out.println("Character data type");
        System.out.println("Size: " + Character.SIZE + " bits");
        System.out.println("Minimum character value: " + Character.MIN_VALUE);
        System.out.println("Maximum character value: " + Character.MAX_VALUE);
        System.out.println("Boolean data type");
        System.out.println("Size: 1 bit");
        System.out.println("Boolean value: " + Boolean.TRUE + " or " + Boolean.FALSE);
    }
}
