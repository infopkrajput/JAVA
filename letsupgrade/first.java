// For n = 3 the pattern will be 
// 3 3 3 2 2 2 1 1 1
// 3 3 2 2 1 1
// 3 2 1
public class first {
    int n;
    public static void main(String[] args) {
        int x = n;
        for(int i = 0;i<n;i++)
        {
            for(int j = 0;i<n;j++)
            {
                System.out.print(x);
            }
        }
        
    }
}