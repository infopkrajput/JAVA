
public class TypeCastingEx1 {

public static void main(String[] args) {
		
		int a=14;
		System.out.println(a);
		
		double b=a;//implicit type casting or upcasting or widening
		
		System.out.println(b);
		
		int c=(int)b;//explicit typecasting oe downcasting or narrowing
		
		System.out.println(c);
	}

}
