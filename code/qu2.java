
public class qu2 {
	    public static void main(String[] args) {
	        String str = "INCAPP";

	        // Loop to print decreasing substrings
	        for (int i = str.length(); i > 0; i--) {
	            // Print the substring from the start to the current index
	            System.out.println(str.substring(0, i));
	        }
	    }
	}


