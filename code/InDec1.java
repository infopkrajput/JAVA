public class InDec1 {

	public static void main(String[] args) {
		int a=14;
		a++;
		System.out.println(a);
		byte a1=14;
		a1++;
		System.out.println(a1);
		short a2=14;
		a2++;
		System.out.println(a2);
		long a3=14;
		a3++;
		System.out.println(a3);
		
		float f1=13.23f;
		f1++;
		System.out.println(f1);
		
		double d1=65.77;
		d1++;
		System.out.println(d1);
		
		char c='h';
		c++;
		System.out.println(c);
	}

}
	